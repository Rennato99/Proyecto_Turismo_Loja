    private void jButtonModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModActionPerformed
        // TODO add your handling code here:
        if (this.jButtonMod.getText().equals("Guardar")) {
            this.jButtonMod.setText("Modificar");
            this.txtIidentification.setEnabled(false);
            this.txtPassword.setEnabled(false);
            this.txtName.setEnabled(false);
            this.txtLastName.setEnabled(false);
            this.txtEmail1.setEnabled(false);
            this.txtNumber.setEnabled(false);
            this.txtPassword.setEnabled(false);
            this.txtPasswordConf.setEnabled(false);

            try {

                Client objClientTemp = ObjBLCli.createClient(this.txtIidentification.getText(), String.valueOf(this.txtPasswordConf.getPassword()),
                        this.txtName.getText(), this.txtLastName.getText(), this.txtEmail1.getText(), this.txtNumber.getText());
                objClient = ObjBLCli.updateClientDB(objClient, objClientTemp);
                JOptionPane.showMessageDialog(null, "ACTUALIZADO CON EXITO");
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            this.jButtonMod.setText("Guardar");
            this.txtIidentification.setEnabled(true);
            this.txtPassword.setEnabled(true);
            this.txtName.setEnabled(true);
            this.txtLastName.setEnabled(true);
            this.txtEmail1.setEnabled(true);
            this.txtNumber.setEnabled(true);
            this.txtPassword.setEnabled(true);
            this.txtPasswordConf.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonModActionPerformed

