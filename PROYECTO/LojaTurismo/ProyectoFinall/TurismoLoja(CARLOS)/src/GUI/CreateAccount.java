    private void btnCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCuentaActionPerformed
        if (ObjBLCli.validadorDeCedula(this.txtIidentification.getText()) || verifyPasswords()) {
            Client auxObjClient = ObjBLCli.createClient(this.txtIidentification.getText(), String.valueOf(this.txtPasswordConf.getPassword()),
                    this.txtName.getText(), this.txtLastName.getText(), this.txtEmail1.getText(), this.txtNumber.getText());

            try {
                ObjBLCli.insertClientDB(auxObjClient);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCrearCuentaActionPerformed

    public void validateLogin() {
        Client objAuxClient = new Client();
        objAuxClient = GlobalVariables.loggedClient;
        if (objAuxClient != null) {
            objClient = objAuxClient;
            this.jButtonMod.setVisible(true);
            this.jButtonMod.setEnabled(true);
            this.btnCancelar.setVisible(true);
            this.btnCancelar.setEnabled(true);
            this.btnCrearCuenta.setVisible(false);
            this.btnCrearCuenta.setEnabled(false);
            this.jButtonSalir.setVisible(false);
            this.jButtonSalir.setEnabled(false);
            this.txtIidentification.setText(objAuxClient.getIdentification());
            this.txtName.setText(objAuxClient.getNames());
            this.txtLastName.setText(objAuxClient.getLastnames());
            this.txtEmail1.setText(objAuxClient.getMail());
            this.txtNumber.setText(objAuxClient.getNumberPhone());
            this.txtPassword.setText(objAuxClient.getPassword());
            this.txtIidentification.setEnabled(false);
            this.txtName.setEnabled(false);
            this.txtLastName.setEnabled(false);
            this.txtEmail1.setEnabled(false);
            this.txtNumber.setEnabled(false);
            this.txtPassword.setEnabled(false);
            this.txtPasswordConf.setEnabled(false);
        } else {
            this.btnCrearCuenta.setVisible(true);
            this.btnCrearCuenta.setEnabled(true);
            this.jButtonSalir.setVisible(true);
            this.jButtonSalir.setEnabled(true);
            this.jButtonMod.setVisible(false);
            this.jButtonMod.setEnabled(false);
            this.btnCancelar.setVisible(false);
            this.btnCancelar.setEnabled(false);
        }
    }
    
    private boolean verifyPasswords(){
        String password = this.txtPassword.getText();
        String confirmPassword = this.txtPasswordConf.getText();
        if(password.equals(confirmPassword)){
            return true;
        }else{
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
            return false;
            
        }
    
    }

